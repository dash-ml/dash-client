    state = {
        src: null,
        lastDrawLocation: null,
        series: recordsToSeries([])
    };

    componentWillMount() {
        // const src = "http://localhost:8082/sample_project/sample_run/data.pkl?json=1";
        const {src} = this.props;
        let _that = this;
        if (src) {
            fetch(src, {headers: {'Content-Type': 'application/json; charset=utf-8'}})
                .then((r) => r.json())
                .then((d) => recordsToSeries(d))
                .then((series) => setTimeout(() => _that.setState({series}), 20));
        }
    }

    render() {
        const {series, lastDrawLocation} = this.state;
        console.log(series);

        return (
            <Flex row>
                <FlexItem component={FlexibleWidthXYPlot}
                          animation
                          xDomain={lastDrawLocation && [lastDrawLocation.left, lastDrawLocation.right]}
                          width={400}
                          height={200}>
                    <VerticalGridLines/>
                    <HorizontalGridLines/>
                    <XAxis/>
                    <YAxis/>
                    {series.map((line) =>
                        <LineSeries key={line.title} data={line.data}/>)}
                    <Highlight onBrushEnd={(area) => {
                        this.setState({lastDrawLocation: area});
                    }}/>
                </FlexItem>
                <FlexItem component={DiscreteColorLegend} width={180} items={series}/>
            </Flex>
        )
    }
